x =int(input("Enter n: ")) #รับค่า x เป็น int เพื่อไปคำนวณในสูตร fibo
fi =(1+(5**0.5))/2 #กำหนดค่า fi เพื่อให้ง่ายต่การแทนค่า
fibo =(((fi)**x/5**0.5)+(1/2)) #แปลงสุตร fibo ให้เป็นแบบตามหลักคณิตของภาษา
print ("fib(%.d) = %.d" %(x,fibo)) #แสดงค่าผลลัพธ์ออกมาเป็น int เพื่อให้ได้จำนวนเต็ม เพราะการหาลำดับจะคำนวนมาได้เป็นทศนิยม